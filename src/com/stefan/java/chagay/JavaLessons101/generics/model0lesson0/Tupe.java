package com.stefan.java.chagay.JavaLessons101.generics.model0lesson0;


public class Tupe {

    private final Object right;
    private final Object left;

    public Tupe(Object right, Object left) {
        this.right = right;
        this.left = left;
    }

    public Object getRight() {
        return right;
    }

    public Object getLeft() {
        return left;
    }
}
